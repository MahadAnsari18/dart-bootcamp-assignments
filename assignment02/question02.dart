/* Q.2: Length and breadth of a rectangle are 5 and 7 respectively.
   Write a program to calculate the area of the rectangle. 
   Formula (area = length * breadth) */

void main() {
  int length = 5;
  int breadth = 7;
  int area = (length * breadth);
  print("Area of a rectangle = $area m²");
}
// Output is:
//           Area of a rectangle = 35 m²