// Q.1: Write a Dart program to check if a given number is positive, negative, or zero.

void main() {
  num digit = 5;
  if (digit < 0) {
    print("The given number is a negative number");
  } else if (digit == 0) {
    print("The given number is zero");
  } else {
    print("The given number is a positive number");
  }
}
// Output is:
//           The given number is a positive number

