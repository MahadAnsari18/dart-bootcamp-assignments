// Q.2: Write a Dart program to check if a given number is even or odd.

void main() {
  num value = 8;
  if (value % 2 == 0) {
    print("The given number $value is an even number");
  } else {
    print("The given number $value is an odd number");
  }
}

// Output is:
//           The given number 8 is an even number
